package org.example.Operators_And_Booleans

//  Augmented assignment operators - Расширенные операторы присваивания

fun main(args: Array<String>){
    var bikes =  5
    println(bikes)
    bikes += 3
    println(bikes)

    println()

    bikes -= 4
    println(bikes)

    println()

    bikes *= 2
    println(bikes)

    println()

    bikes /= 2
    println(bikes)

    println()

    bikes %= 2
    println(bikes)
}