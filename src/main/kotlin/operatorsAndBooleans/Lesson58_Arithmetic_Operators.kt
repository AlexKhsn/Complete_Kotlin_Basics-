package operatorsAndBooleans

fun main(Args: Array<String>) {
    println(5 + 2)
    println("Alex" + " Khomishyn")

    println(5 - 2)

    println(5 * 2)

    println(5 / 2)
    println(5f / 2f)

    println(5 % 2)

    var cats = 5
    cats++
    println("Now I have $cats cats")

    cats--
    println("After one cat run away, I have $cats cats again")

    println(cats++)
    println(cats)

    println(++cats)

    println(cats--)
    println(cats)

    println(--cats)
}
